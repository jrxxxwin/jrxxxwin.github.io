function signup() {
    const sign = document.getElementById('signSide')
    const login = document.getElementById('loginSide')

    login.style.display = 'none'
    sign.style.display = 'flex'
}
function logins() {
    const sign = document.getElementById('signSide')
    const login = document.getElementById('loginSide')
    
    sign.style.display = 'none'
    login.style.display = 'flex'
}